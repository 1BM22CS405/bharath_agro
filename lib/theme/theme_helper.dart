        ),
        displayMedium: TextStyle(
          color: appTheme.whiteA700,
          fontSize: 44.fSize,
          fontFamily: 'Comfortaa',
          fontWeight: FontWeight.w700,
        ),
        displaySmall: TextStyle(
          color: colorScheme.onErrorContainer.withOpacity(1),
          fontSize: 36.fSize,
