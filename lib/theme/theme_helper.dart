/// Class containing the supported color schemes.
class ColorSchemes {
  static final primaryColorScheme = ColorScheme.light(
    // Primary colors
    primary: Color(0XFF1D3133),
    primaryContainer: Color(0XFF1BB63D),

    // Error colors
    onErrorContainer: Color(0XCCDBBF9D),

    // On colors(text colors)
    onPrimary: Color(0X3F000000),
    onPrimaryContainer: Color(0XFF191D1E),
  );
}

/// Class containing custom colors for a primary theme.
class PrimaryColors {
  // BlueGray
  Color get blueGray800 => Color(0XFF29595E);

  // BlueGrayEf
  Color get blueGray900Ef => Color(0XEF244542);

  // Gray
  Color get gray100 => Color(0XFFF3F2F6);

  // White
  Color get whiteA700 => Color(0XFFFFFFFF);

  Color get black => Color(0X00000000);
}

PrimaryColors get appTheme => ThemeHelper().themeColor();
ThemeData get theme => ThemeHelper().themeData();
